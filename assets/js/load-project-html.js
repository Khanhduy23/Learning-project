function createCard(card) {
    return `
        <section class="card">
            <a class="card__link" href="${card.link}">
                <div class="card__content">
                    <div class="card__icon">
                        <i class="${card.icon}"></i>
                    </div>
                    <div class="card__title">${card.title}</div>
                </div>
            </a>
        </section>
    `;
}

function loadCards() {
    const tutorialContainer = document.getElementById('tutorial-cards');
    const formContainer = document.getElementById('form-cards');
    const grapContainer = document.getElementById('grap-cards');
    const mediaContainer = document.getElementById('media-cards');
    const apisContainer = document.getElementById('apis-cards');
    const examplesContainer = document.getElementById('examp-cards');

    // Chèn dữ liệu cho phần HTML Tutorial
    tutorialCards.forEach(card => {
        tutorialContainer.innerHTML += createCard(card);
    });

    // Chèn dữ liệu cho phần HTML Form
    formCards.forEach(card => {
        formContainer.innerHTML += createCard(card);
    });
    // Chèn dữ liệu cho phần HTML Graphics
    grapCards.forEach(card => {
        grapContainer.innerHTML += createCard(card);
    })
    // Chèn dữ liệu cho phần HTML Media
    mediaCards.forEach(card => {
        mediaContainer.innerHTML += createCard(card);
    });
    // Chèn dữ liệu cho phần HTML APIs
    apisCards.forEach(card => {
        apisContainer.innerHTML += createCard(card)
    });
    examCards.forEach(card => {
        examplesContainer.innerHTML += createCard(card);
    });
}

// Gọi hàm để tải thẻ vào phần
loadCards();